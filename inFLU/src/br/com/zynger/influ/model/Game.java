package br.com.zynger.influ.model;

import java.io.Serializable;
import java.util.Date;

public class Game implements Serializable{
	
	private static final long serialVersionUID = 1L;
	private String competition;
	private Club club1, club2;
	private String stadium;
	private Date date;
	private String referee, aux1, aux2;
	private String transmission;
	
	public Game(String competition, Club home, Club away, String stadium, Date date,
			String referee, String aux1, String aux2, String transmission){
		setCompetition(competition);
		setClub1(home);
		setClub2(away);
		setStadium(stadium);
		setDate(date);
		setReferee(referee);
		setAux1(aux1);
		setAux2(aux2);
		setTransmission(transmission);
	}
	
	public String getCompetition() {
		return competition;
	}
	
	public void setCompetition(String competition) {
		this.competition = competition;
	}
	
	public Club getClub1() {
		return club1;
	}
	
	public void setClub1(Club club1) {
		this.club1 = club1;
	}
	
	public Club getClub2() {
		return club2;
	}
	
	public void setClub2(Club club2) {
		this.club2 = club2;
	}
	
	public String getStadium() {
		return stadium;
	}
	
	public void setStadium(String stadium) {
		this.stadium = stadium;
	}
	
	public Date getDate() {
		return date;
	}
	
	public void setDate(Date date) {
		this.date = date;
	}
	
	public String getReferee() {
		return referee;
	}
	
	public void setReferee(String referee) {
		this.referee = referee;
	}
	
	public String getAux1() {
		return aux1;
	}
	
	public void setAux1(String aux1) {
		this.aux1 = aux1;
	}
	
	public String getAux2() {
		return aux2;
	}
	
	public void setAux2(String aux2) {
		this.aux2 = aux2;
	}
	
	public String getTransmission() {
		return transmission;
	}
	
	public void setTransmission(String transmission) {
		this.transmission = transmission;
	}
}
